# Funcion para limpiar tuit, regresa un string limpio
def clean_tuit(tuit):
    stop_words = set(stopwords.words('english'))
    word_tokens = word_tokenize(tuit)

    filtered_sentence = [w for w in word_tokens if not w in stop_words]
    filtered_sentence = []

    for w in word_tokens:
        if w not in stop_words:
            filtered_sentence.append(w)

    return " ".join(filtered_sentence)

# Funcion para clasificar tuits
def clasificar_tuits(tuits, tweets):
	predictions = {}
	train_set = featureSet()
	classifier = NaiveBayesClassifier.train(train_set)
	for sentence in tuits:
		sentence = sentence.lower()
		words = sentence.split(' ')
		featureset = {}
		for word in words:
			featureset.update(word_feats(word))
		prediction = classifier.classify(featureset)
		predictions[sentence] = prediction
	print("{:<10} {:<15}".format('Categorias','Tuit'))
	pos=0
	for k, v in predictions.items():
		print("{:<10} {:<15}".format(v, tweets[pos]))
		pos+=1
